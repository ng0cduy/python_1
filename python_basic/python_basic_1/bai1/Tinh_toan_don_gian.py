# Giá trị x y cho trước, Học viên có thể thay đổi
x, y = 20, 6

# In ra giá trị x, y
print ('x =', x, ', y =', y)

# Tính tổng x + y
print ("Tổng x+y =", x + y)

# Tính hiệu x - y
print ("Hiệu x-y",x-y)

# Tính tích x * y
print ("Tích x*y",x*y)

# Tính thương  x/ y
print ("Thương x/y",x/y)

