x = 15
y = 12
print('Binary of x =', bin(x), ', Binary of y =', bin(y)) # x=0b1111, y=0b1100
print('x & y =', bin(x & y))        # 0b1100
print('x | y =', bin(x | y))        # 0b1111
print('x ^ y =', bin(x ^ y))        # 0b0011
print('~x =', bin(~x))              # 0b0000
print('x << 2 =', bin(x << 2))      # 0b111100
print('y >> 2 =', bin(y >> 2))      # 0b0011

